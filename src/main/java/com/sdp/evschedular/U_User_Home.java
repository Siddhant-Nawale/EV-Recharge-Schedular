    public U_User_Home(String s) {
        initComponents();
        cid= s;
        
        String query = String.format("SELECT `Fname` , `Lname`, `Uname` ,`Gender`, `Contact` FROM `Customers` WHERE `Customer_ID` = '"+cid+"'");
        ResultSet r;
        try {
            Statement st = M_Connector.getconnection().createStatement();
            r = st.executeQuery(query);
            
            if(r.next())
            {
                Name.setText(r.getString("Fname")+" "+r.getString("Lname"));
                Contact.setText(r.getString("Contact"));
                Gender.setText(r.getString("Gender"));
                Uname.setText(r.getString("Uname"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(U_User_Home.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

