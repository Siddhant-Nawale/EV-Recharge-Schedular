    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        
        String uname = Uname.getText();
        String pass = String.valueOf(Password.getPassword());

        Statement st;
        String query = "SELECT * FROM `Customers` WHERE `Uname`= '"+uname+"' AND `Password`= '"+pass+"'";
        ResultSet re ;
        
        try {
            st = M_Connector.getconnection().createStatement();
            re = st.executeQuery(query);
            if(re.next()){
                
                U_User_Home userhome = new U_User_Home(re.getString("Customer_ID"));
                userhome.setVisible(true);
                userhome.pack();
                userhome.setLocationRelativeTo(null);
                userhome.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
                
            }
            else if(true){
                re = st.executeQuery("SELECT * FROM `Venders` WHERE `Uname`= '"+uname+"' AND `Password`= '"+pass+"'");
                if(re.next()){
                    O_Owners_Home home = new O_Owners_Home(re.getString("Vender_ID"));
                    home.setVisible(true);
                    home.pack();
                    home.setLocationRelativeTo(null);
                    home.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Invalid Credentials");
                }
            }
            else{
                    JOptionPane.showMessageDialog(null, "Invalid Credentials");
                }
            }catch (SQLException ex) {
            Logger.getLogger(U_User_Booking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

